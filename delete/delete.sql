-- Delete all records from a table
-- In SQL, the DELETE clause is used to delete records from a table. When the 
DELETE FROM table_name 
-- statement is executed, all records in the specified table are deleted.

-- It is important to note that this operation is irreversible and will permanently delete the data from the table, so you must be very careful when using this statement.

-- Exercises
-- Delete all data from the 'products' table.


DELETE FROM products