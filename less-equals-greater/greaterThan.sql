-- USING THE GREATER THAN OR EQUAL TO OPERATOR
-- The greater than or equal to operator (>=) is used to select records where the value of a column is greater than or equal to a specific value. For example, we can select all products whose price is greater than or equal to $100 using the following query:

-- SELECT * FROM products WHERE price >= 100;
-- Exercise Select all records from the products table where the value of the 'price' column is greater than or equal to 50.

